package com.xunwei.som.pojo;

public class CompInfo {
    private Integer compNumber;

    private String compName;

    private String compLocation;
    
    private String abbreviation;
    
    private Integer display;

	public Integer getCompNumber() {
		return compNumber;
	}

	public void setCompNumber(Integer compNumber) {
		this.compNumber = compNumber;
	}

	public String getCompName() {
		return compName;
	}

	public void setCompName(String compName) {
		this.compName = compName;
	}

	public String getCompLocation() {
		return compLocation;
	}

	public void setCompLocation(String compLocation) {
		this.compLocation = compLocation;
	}

	public String getAbbreviation() {
		return abbreviation;
	}

	public void setAbbreviation(String abbreviation) {
		this.abbreviation = abbreviation;
	}

	public Integer getDisplay() {
		return display;
	}

	public void setDisplay(Integer display) {
		this.display = display;
	}
	
}